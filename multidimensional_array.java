public class multidimensional_array {
    public static void main(String[] args) {
        // create 2-D array
        int [][] a={
            {12,45,50},
            {120,19,12,10},
            {13,56},
        };
        // calculate the length of the array
        System.out.println(a[0].length);
        System.out.println(a[1].length);
        System.out.println(a[2].length);
    }
}
